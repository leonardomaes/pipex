#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*temp;

	if (!lst || !new)
		return ;
	if (*lst == NULL)
	{
		*lst = new;
		return ;
	}
	temp = *lst;
	if (temp->next != NULL)
		ft_lstadd_back(&temp->next, new);
	else
		temp->next = new;
}
